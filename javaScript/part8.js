let level = 4;

switch (level) {
  case 1:
    console.log("Ang pinindot mo ay ang number 1");
    break;
  case 2:
    console.log("Ang pinindot mo ay number 2");
    break;
  case 3:
    console.log("Ang pinindot mo ay number 3");
    break;
  default:
    console.log("Tang ina mo wala yan sa nabanggit!");
    break;
}

